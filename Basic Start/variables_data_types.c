#include <stdio.h>

int main() {
    int myAge = 18;            // Integer (whole number)
    float myGPA = 8.5;         // Floating point number
    char myGrade = 'A';        // Character

    printf("I am %d years old.\n", myAge);
    printf("My GPA is %.2f.\n", myGPA);
    printf("My grade is %c.\n", myGrade);

    return 0;
}
